#include "../matft.h"

t_mat3	mat3(t_vec3 l1, t_vec3 l2, t_vec3 l3);
t_mat3	mat2to3(t_mat2 m);

t_mat3	mat3(t_vec3 l1, t_vec3 l2, t_vec3 l3)
{
	t_mat3	r;

	r.l1 = l1;
	r.l2 = l2;
	r.l3 = l3;
	return (r);
}

t_mat3	mat2to3(t_mat2 m)
{
	return (mat3(
			vec2to3(m.l1, 0.),
			vec2to3(m.l2, 0.),
			vec3(0., 0., 1.)
		));
}
